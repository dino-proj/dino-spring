package com.botbrain.dino.utils.flan;

/**
 * the log is abandoned for some reason,
 * @author Administrator
 *
 */
public class AbandonedException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AbandonedException() {
		super();
	}

	public AbandonedException(String message, Throwable cause) {
		super(message, cause);
	}

	public AbandonedException(String message) {
		super(message);
	}

	public AbandonedException(Throwable cause) {
		super(cause);
	}

}
